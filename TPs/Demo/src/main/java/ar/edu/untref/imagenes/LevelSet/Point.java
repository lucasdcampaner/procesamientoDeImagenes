package ar.edu.untref.imagenes.LevelSet;

public class Point {
    /**
     * X coordinate
     */
    public int x;

    /**
     * Y coordinate
     */
    public int y;

    /**
     * Constructor
     * @param x The X coordinate
     * @param y The Y coordinate
     */
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}